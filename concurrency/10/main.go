package main

import (
	"fmt"
	"sync"
	"time"
)

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

// VisitedURLs holds whether the URL has already been visited.
type VisitedURLs struct {
	v   map[string]bool
	mux sync.Mutex
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func Crawl(url string, depth int, fetcher Fetcher, visitedURLs *VisitedURLs) {
	// TODO: Fetch URLs in parallel.
	// TODO: Don't fetch the same URL twice.
	// This implementation doesn't do either:
	if depth <= 0 {
		return
	}

	// Check if the URL has been visited.
	visitedURLs.mux.Lock()
	visited := visitedURLs.v[url]
	visitedURLs.mux.Unlock()
	if visited {
		fmt.Println("already visited", url)
		return
	}
	visitedURLs.mux.Lock()
	visitedURLs.v[url] = true
	visitedURLs.mux.Unlock()

	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}
	fmt.Printf("found: %s %q\n", url, body)
	for _, u := range urls {
		go Crawl(u, depth-1, fetcher, visitedURLs)
	}
	return
}

func main() {
	visitedURLs := VisitedURLs{
		v: make(map[string]bool),
	}
	go Crawl("https://golang.org/", 4, fetcher, &visitedURLs)
	time.Sleep(time.Second)
}

